package operadores;

public class OperadoresLogicos {
    public static void main(String[] args) {
//        Son los booleanos
        int x = 2;
        int y = 4;
        int z = 2;

        System.out.println(y > x);
        System.out.println(y < x);
        System.out.println(x > z);
        System.out.println(x < z);
//         le ponemos dos iguales porque lo estamos comparando
        System.out.println(x == z);
        System.out.println(x >= z);
        System.out.println(x <= z);
//        para comprobar si son diferentes
        System.out.println(x != y);


    }
}
